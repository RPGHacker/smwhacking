<h2>Was ist SMW-Hacken?</h2>

<p><em>SMW</em> steht für <em>Super Mario World</em>, ein Spiel aus dem Jahr 1991 für die Spielekonsole <em>SNES</em>
	(Super Nintendo).</p>
<div class="image-container left">
	<img src="img/snes9x.png" alt="Der Emulator snes9x" />
	<p>Der Emulator snes9x</p>
</div>
<p>Früher brauchte man, um Super Mario World zu spielen, noch eine echte Konsole, das echte Spielmodul und einen
	Fernseher, aber heutzutage kann man SMW auch auf dem Computer spielen! Das geht mit sogenannten <em>Emulatoren</em>
	&mdash; das sind Programme, die auf einem Computer eine Spielekonsole simulieren. Ein beliebter Emulator für die
	SNES ist zum Beispiel <em>snes9x</em>, aber es gibt eine große Auswahl an SNES-Emulatoren (natürlich auch Emulatoren
	für andere Systeme).<br />
	Anstatt eines Spielmoduls gibt es dann so genannte <em>ROM</em>s &mdash; das sind Dateien auf dem Computer, die ganz
	genau dasselbe enthalten wie die handfesten Spielmodule von damals, die Grafiken, den Programmcode und all das.
	Möchte man ein Konsolenspiel auf dem Computer spielen, braucht man dann nur noch die ROM mit dem Emulator zu öffnen.
</p>
<p>Und so wie man jede Datei kopieren, löschen oder verändern kann, so klappt das auch mit ROMs! Eine ROM zu
	verändern heißt das Spiel zu verändern und so sein eigenes daraus zu machen. Das ist von Hand natürlich
	ziemlich schwer, aber für viele Spiele gibt es <em>Level-Editoren</em>, mit denen man die Level ganz einfach mit
	einigen Mausklicks umbauen kann.</p>
<div class="image-container right">
	<img src="img/lm.png" alt="Ein Ausschnitt aus Lunar Magic" />
	<p>Ein Ausschnitt aus Lunar Magic</p>
</div>
<p>So einen Editor gibt es auch für Super Mario World &mdash; er heißt <em>Lunar Magic</em>, oft auch kurz <em>LM</em>.
	Dieses Programm kann die ROM öffnen und sehr einfach verändern: Lunar Magic macht es möglich, alle Level zu
	bearbeiten, die Farben zu ändern, eine neue Weltkarte zu basteln, die Grafiken zu verändern und noch einiges mehr.
	Daneben gibt es auch noch eine Reihe von Tools, die das bearbeiten können, was Lunar Magic nicht kann &mdash; zum
	Beispiel selbst gemachte Objekte und Gegner ins Spiel einfügen, oder sogar neue Musik. Auf diese Weise kann man aus
	der ROM von SMW sein ganz eigenes Mario-Spiel erstellen.</p>
<p>Das Verändern einer ROM wird <em>Hacken</em> (englisch <em>Hacking</em>) genannt, und das Spiel, das man daraus
	macht, heißt <em>Hack</em>. Das hat nichts mit dem "Einhacken" in fremde Computer zu tun, aber aufpassen sollte man
	trotzdem &mdash; ROM-Dateien weiterzugeben (originale und auch bearbeitete) oder Links zu Websites, die ROMs
	anbieten, wird hier und auch anderswo nicht gern gesehen.<br />
	Deshalb musst du, um einen Hack zu veröffentlichen, aus deiner ROM einen <em>Patch</em> erstellen. Das kannst du zum
	Beispiel mit einem Programm namens <em>Lunar IPS</em>.</p>
<p>Mehr übers Hacken, wo man die Tools herbekommt und wie man einen Patch erstellt, kannst du im Tutorial-Bereich des
	Forums oder auf der englischsprachigen Seite <a href="http://www.smwcentral.net">SMW Central</a> herausfinden.
</p>